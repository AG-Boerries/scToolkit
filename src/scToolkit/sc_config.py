def dataset_to_dict(
            path: str,
            exclude_reference: bool = True,
            return_regex: bool = False,
        ) -> dict[str, list[str]]:
    """Creates a CSV of columns of genes to a dictionary.

    NOTE:
        - this will exclude the column "reference" because we use it to list the
          papers to cite for each column!
        - CSV stands for COMMA separeted, no excel or tab or whatever separated!

    Args:
        path (str): Path to the CSV
        exclude_reference (bool, optional): If you want to not exclude a colum
            named "reference", use at your own risk. Defaults to True.

    Returns:
        dict[str, list[str]]:
            dict[str, list[str]]

    Called By:
        get_config

    Tags:
        annotation, io
    """
    dict_ = {}
    genesets = pd.read_csv(path)
    if exclude_reference:
        genesets = genesets[[k for k in genesets.columns if k != "references"]]
    for gs_name in genesets.keys():
        if return_regex:
            dict_[gs_name] = "$|^".join(
                    [g.replace(" ", "").replace(".", "/.")
                        for g in genesets[gs_name].dropna()])
            dict_[gs_name] = "^" + dict_[gs_name][1:-1] + "$"
        else:
            dict_[gs_name] = genesets[gs_name].dropna().tolist()
    return dict_


