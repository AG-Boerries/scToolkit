# GPU handling
def change_gpu_usage(
            adata: object,
            enable: bool = True,
            to_enable: list[str] = ["general", "pca", "leiden", "neighbors", "umap"]
        ) -> None:
    """
    Enables or disables GPU support globally or for specific operations in
    an adata object.

    to_enable=["global"], enable=False, would be global disabling
    to_enable=["global"], enable=True, would NOT be global enabling.
    therefore you have to use to_enable=["general", "pca", "leiden", "neighbors", "umap"]

    This function modifies the configuration settings in ``adata.uns`` to enable
    or disable GPU acceleration for selected preprocessing and analysis methods,
    including PCA, Leiden clustering, neighbor graph construction, and UMAP.

    NOTE:
        This function assumes that ``adata.uns["config"]`` follows a specific
        hierarchical structure with keys for preprocessing (``pp``) and tools
        (``tl``). Ensure that this structure exists before calling this function.

    Args:
        adata (object): adata object containing configuration settings in
            ``adata.uns``.
        enable (bool, optional): Whether to enable (True) or disable (False) GPU
            support. Defaults to True.
        to_enable (list[str], optional): List of operations to modify. Supported
            options are "pca", "leiden", "neighbors", and "umap".
            Defaults to ["pca", "leiden", "neighbors", "umap"].

    Returns:
        None

    Tags:
        config
    """
    # #########################################################
    # Set general GPU usage flag in the configuration
    if "general" in to_enable:
        adata.uns["config"]["general"]["use_GPU"] = "True" if enable else "False"
    # #########################################################
    # Configure GPU acceleration for individual components
    # ##########################################
    # Configure PCA computation method
    if "pca" in to_enable:
        adata.uns["config"]["pp"]["pca"]["method"] = "rapids" if enable else "pca"
    # ##########################################
    # Configure Leiden clustering method
    if "leiden" in to_enable:
        adata.uns["config"]["tl"]["umap"]["method"] = "rapids" if enable else "umap"
    # ##########################################
    # Configure nearest neighbors computation method
    if "neighbors" in to_enable:
        adata.uns["config"]["tl"]["leiden"]["method"] = "rapids" if enable else "leiden"
    # ##########################################
    # Configure UMAP embedding method
    if "umap" in to_enable:
        adata.uns["config"]["pp"]["neighbors"]["transformer"] = "rapids" if enable else "sklearn"


