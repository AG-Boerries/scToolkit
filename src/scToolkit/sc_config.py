def update_nested_dict(
            nested_dict: dict,
            nested_dict_update: dict
        ) -> dict:
    """Updates a nested dictionary with values from another nested dictionary.

    This function recursively updates a nested dictionary ``nested_dict`` with the
    values from another nested dictionary ``nested_dict_update``. If the same key
    exists in both dictionaries and the value associated with that key is also a
    dictionary, it will merge the dictionaries rather than overwriting. This
    allows for deep merging of nested dictionaries.

    NOTE:
        This function is similar to dict.update(other_dict), but it supports
        nested dictionaries.

    Example use case:
        Consider a scenario where you have a default configuration dictionary
        and you want to update it with user-specified overrides, while
        preserving the nested structure.

    Args:
        nested_dict (dict): The dictionary to update. This is the dictionary
            that will be modified.
        nested_dict_update (dict): The dictionary where the update comes from.
            This contains the new values that will be merged into ``nested_dict``.

    Returns:
        dict:
            The updated dictionary with the merged values from
            ``nested_dict_update``.

    References:
        https://stackoverflow.com/questions/3232943/update-value-of-a-nested-dictionary-of-varying-depth

    Calls:
        update_nested_dict

    Called By:
        load_h5ad_old, update_nested_dict

    Tags:
        utils
    """
    # #########################################################
    # Iterate through the items of nested_dict_update to merge into nested_dict
    for k, v in nested_dict_update.items():
        # ##########################################
        # Check if the value is a mapping to handle nested dictionaries
        if isinstance(v, Mapping):
            # Recursively update the nested dictionary
            nested_dict[k] = update_nested_dict(nested_dict.get(k, {}), v)
        else:
            # ##########################################
            # Update the key with the new value if it's not a mapping
            nested_dict[k] = v
    # #########################################################
    # Return the updated dictionary
    return nested_dict


def update_nested(
            base: dict | list,
            updates: dict | list
        ) -> None:
    """
    Recursively updates a nested dictionary or list structure with values from
    another dictionary or list.

    This function modifies the ``base`` structure in place by merging values from
    ``updates``. If both ``base`` and ``updates`` contain dictionaries, they are
    recursively merged. If they contain lists, corresponding elements are
    updated where possible. If the index in ``updates`` exceeds the length of
    ``base``, the new elements are appended.

    NOTE:
        - The function modifies the ``base`` object in place and does not return a
          new structure.
        - If ``updates`` contains new keys or indices, they are added to ``base``.
        - Lists are updated by modifying their corresponding elements or
          appending new ones.

    Args:
        base (dict | list): The original dictionary or list to be updated.
        updates (dict | list): The dictionary or list containing updates.

    Returns:
        None

    Raises:
        TypeError: If either ``base`` or ``updates`` is not a dictionary or a list.

    Calls:
        update_nested

    Called By:
        update_nested

    Tags:
        utils
    """
    # #########################################################
    # Ensure ``base`` and ``updates`` are both valid dictionary or list structures
    if not isinstance(base, (dict, list)) or not isinstance(updates, (dict, list)):
        raise TypeError("Both ``base`` and ``updates`` must be either a dictionary or a list.")
    # #########################################################
    # Handle dictionary-based recursive updates
    if isinstance(base, dict) and isinstance(updates, dict):
        for k, v in updates.items():
            if k in base:
                # ##########################################
                # If both values are dictionaries, merge recursively
                if isinstance(base[k], dict) and isinstance(v, dict):
                    update_nested(base[k], v)
                # If both values are lists, merge recursively
                elif isinstance(base[k], list) and isinstance(v, list):
                    update_nested(base[k], v)
                # Otherwise, overwrite the existing value
                else:
                    base[k] = v
            else:
                # ##########################################
                # If the key is not present in ``base``, add it
                base[k] = v
    # #########################################################
    # Handle list-based recursive updates
    elif isinstance(base, list) and isinstance(updates, list):
        for i, item in enumerate(updates):
            if i < len(base):
                # ##########################################
                # If both values are dictionaries, merge recursively
                if isinstance(base[i], dict) and isinstance(item, dict):
                    update_nested(base[i], item)
                # If both values are lists, merge recursively
                elif isinstance(base[i], list) and isinstance(item, list):
                    update_nested(base[i], item)
                # Otherwise, overwrite the existing value
                else:
                    base[i] = item
            else:
                # ##########################################
                # Append new elements if the list index exceeds ``base``
                base.append(item)


def get_nested_dict_keys_structure(
            dict_: dict
        ) -> dict:
    """Recursively extracts the key structure of a nested dictionary.

    Use-Case: For exploring the experiment_dict config like
        get_nested_dict_keys_structure(config["general"])

    Args:
        dict_ (dict): The dictionary whose key structure should be extracted.

    Returns:
        dict:
            A dictionary reflecting only the key structure of the input.

    .. doctest::

        >>> keys_only_dict = get_nested_dict_keys_structure(adata.uns["config"]["general"])
        >>> print(keys_only_dict)

    Calls:
        get_nested_dict_keys_structure

    Called By:
        get_nested_dict_keys_structure

    Tags:
        utils
    """
    if not isinstance(dict_, dict):
        return  # Or you could return the value itself if you want to show non-dict values

    keys_only = {}
    for key, value in dict_.items():
        if isinstance(value, dict):
            keys_only[key] = get_nested_dict_keys_structure(value)
        else:
            keys_only[key] = {}  # Represents a leaf node (a value was here)
    return keys_only


def update_key_in_config(
            config: dict,
            key_to_update: str,
            new_value: object
        ) -> None:
    """
    Recursively update all keys named 'key_to_update' in a nested config
    dictionary if their type matches the type of 'new_value' (or they are None).

    Args:
        config (dict): The configuration dictionary to modify in-place.
        key_to_update (str): The key name to update.
        new_value (object): The new value to assign.

    Returns:
        None

    Calls:
        update_key_in_config

    Called By:
        update_key_in_config

    Tags:
        config, utils
    """
    for key, value in config.items():
        if isinstance(value, dict):
            update_key_in_config(value, key_to_update, new_value)
        elif key == key_to_update:
            if isinstance(value, type(new_value)) or value is None:
                config[key] = new_value
