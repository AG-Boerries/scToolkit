class experiment_dict(dict):
    """This class stores the parameter setup for a single-cell experiment.

    The experiment_dict class is designed to handle various configuration
    settings for a single-cell experiment, including quality control (QC)
    parameters, downstream analysis setups, and plot settings. The dictionary is
    structured to accommodate different stages of the data processing pipeline,
    with distinct sections for each major step.

    Attributes:
        general (dict): A dictionary to store general experiment settings.
        qc (dict): A dictionary to store quality control parameters, including
            gene identifiers.
        downstream (dict): A dictionary to store downstream analysis
            configurations.
        scToolkit (dict): A dictionary to store settings related to the Scanpy
            API.
        to_plot (dict): A nested dictionary to store plotting configurations for
            different stages.
        to_create (dict): A nested dictionary to store configurations for items
            to be created, such as DEG CSVs.

    Returns:
        None

    Tags:
        config
    """
    def __init__(self) -> None:
        """Initializes the experiment_dict with predefined sections for different processing steps."""
        super().__init__()
        # #########################################################
        # Create the general dictionary
        self["general"] = {}
        # #########################################################
        # Create dictionaries for different processing steps
        # qc: Quality control section, includes gene identifiers
        self["qc"] = {"gene_identifiers": {}}

        # downstream: Downstream analysis section
        self["downstream"] = {}

        # scToolkit: scToolkit related settings
        self["scToolkit"] = {}
        # #########################################################
        # Creating nested dictionaries for plotting and creating outputs
        # TODO: move this outside of the get_dict, but into the init.
        self["to_plot"] = {
            "qc": {},
            "cl": {},
            "down": {
                "marker_dendrograms": {},
                "gradients_umap": {"include_mod_variables": {}},
                "cluster_dotplot": {},
                "rank_genes_groups": {},
                "cell_type_score_umap": {},
                "cluster_violins": {},
                "heatmaps": {}}}
        self["to_create"] = {
            "down": {
                "DEG_csv": {},
                "marker_csv": {},
                "cluster_stats_csv": {}}}

    def set_save_path(
                self,
                save_path: str,
                overwrite: bool
            ) -> None:
        """Sets the save path for the experiment data.

        Args:
            save_path (str): The directory path where the experiment data should be saved.
            overwrite (bool): Flag indicating whether to overwrite existing directories.

        Raises:
            OSError: If the directory cannot be created and does not already exist.

        NOTE:
            The save_path is trimmed of trailing slashes and updated with a unique identifier if
            overwrite is set to False and the directory already exists.
        """
        # #########################################################
        # Normalize save_path by removing trailing slash
        if save_path[-1] == "/":
            save_path = save_path[:-1]
        # #########################################################
        # Ensure unique save path if not overwriting
        i = 0
        while os.path.exists(save_path) and not overwrite:
            if i > 0:
                save_path = save_path[:-2]
            save_path += f'_{i}'
            i += 1
        save_path += "/"
        # #########################################################
        # Create the directory if it doesn't exist
        if not os.path.exists(save_path):
            os.makedirs(save_path)

        self["general"]["save_path"] = save_path

    def sync_dict(self, dictionary: dict) -> None:
        """Synchronizes the current experiment dictionary with another dictionary.

        Args:
            dictionary (dict): The dictionary to sync with.

        NOTE:
            DEPRECATED!!! This method resets the save path and calculates
            non-overlapping keys between the current dictionary and the provided
            one. The non-overlapping keys are stored under 'non_overlap'.
        """
        # #########################################################
        # Reset the save path using the provided dictionary
        self.set_save_path(dictionary["general"]["save_path"], dictionary["general"]["overwrite"])
        # #########################################################
        # Identify and store non-overlapping keys
        non_overlap = [k for k in dictionary.keys() if k not in self.keys()]
        if len(non_overlap) > 0:
            logger.info("There are non overlapping keys while syncing. The keys are saved in 'non_overlap'")
            self["non_overlap"] = non_overlap
        # #########################################################
        # Replace the current dictionary elements with the provided dictionary's elements
        for k, v in dictionary.items():
            self[k] = v


def get_config(
            param_setup: str = "default",
            experiment_name: str = "default",
            dataset_name: str = "default",
            save_path: str = "./analysis/",
            use_rna: bool = True,
            use_prot: bool = False,
            analysis_only: bool = False,
            overwrite: bool = True,
            marker_genes: list[str] = [],
            organism: str = "human",
            use_highly_variable_plus_marker_genes: bool = False,
            use_GPU: bool = True,
            n_cores: int = 20,
            selected_citeseq_markers: list[str] = [],
            selected_gene_markers: list[str] = []
        ) -> dict:
    """Generates a configuration dictionary for single-cell analysis.

    This function initializes and returns a configuration dictionary for setting
    up a single-cell analysis pipeline. The configuration includes general
    processing parameters, dataset-specific settings, quality control settings,
    clustering preferences, and visualization options.

    NOTE:
        - Scaled values are computed as z-score transformations and are useful
          for dot plots or heatmaps.
        - If ``use_highly_variable_plus_marker_genes`` is set, marker genes will
          be included in the highly
          variable gene selection.
        - GPU processing is available but starts with clustering.

    Args:
        param_setup (str, optional): Defines the parameter setup name.
            Defaults to "default".
        experiment_name (str, optional): Name of the experiment.
            Defaults to "default".
        dataset_name (str, optional): Name of the dataset.
            Defaults to "default".
        save_path (str, optional): Directory path to save outputs.
            Defaults to "./analysis/".
        use_rna (bool, optional): Whether to use RNA-based analysis. Default is
            True. Defaults to True.
        use_prot (bool, optional): Whether to include protein-based analysis.
            Default is False. Defaults to False.
        analysis_only (bool, optional): If True, skips all plotting. Default is
            False. Defaults to False.
        overwrite (bool, optional): If True, overwrites existing results.
            Default is True. Defaults to True.
        marker_genes (list[str], optional): List of marker genes to consider.
            Defaults to [].
        organism (str, optional): Specifies the organism (e.g., 'human',
            'mouse'). Default is 'human'. Defaults to "human".
        use_highly_variable_plus_marker_genes (bool, optional): If True,
            includes marker genes in highly variable genes. Defaults to False.
        use_GPU (bool, optional): Whether to use GPU acceleration. Default is
            True. Defaults to True.
        n_cores (int, optional): Number of CPU cores to use. Default is 20.
            Defaults to 20.
        selected_citeseq_markers (list[str], optional): Selected CiteSeq
            markers. Defaults to [].
        selected_gene_markers (list[str], optional): Selected gene markers.
            Defaults to [].

    Returns:
        dict:
            A configuration dictionary for the analysis pipeline.

    Raises:
        NotImplementedError: If an unsupported organism is provided.

    Calls:
        dataset_to_dict, experiment_dict.set_save_path

    Called By:
        load_h5ad_old, setup_adata

    TODO:
        Implement organism support for species other than human and mouse.

    Tags:
        DEG, QC, annotation, clustering, config, normalization, scaling, stats,
        visualization
    """
    config = experiment_dict()
    # #############################################
    # Super General stuff!
    # Please take time to make sure to set this parameters right
    # Key for differentiating the samples/replicates
    sample_key = []
    # Key for the mitochondiral genes
    mitochondiral_key = "MT"
    # Key for the Ribosomal Genes
    ribosomal_key = "RBS"
    # Key for the Mito-Ribosomal genes
    mito_ribosomal_key = "MTRBS"
    # Key for the pseudo genes
    pseudogene_key = "PSEUDO"
    # Key for the Housekeeping Genes
    house_keeping_key = "HK"
    # Key for the Hemoglobin Genes
    hemoglobin_key = "HG"

    # Default color map, if R use the R default
    cmap = "R"
    # Check if colormap exist
    if cmap not in colormaps() or cmap == "R":
        cmap = "R"

    # General Stuff
    # which layers should be calculated and saved
    config["general"]["n_cores"] = n_cores
    config["general"]["save_counts"] = True
    config["general"]["save_norm_counts"] = True
    # You should save this for sure to replace X after clustering with the unscaled version
    config["general"]["save_normlog2_counts"] = True
    config["general"]["save_regress_out"] = False
    # Optionaly save the non normed log1p counts
